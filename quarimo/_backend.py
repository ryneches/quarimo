# Module-level singleton — the single source of truth for backend availability
backends = _BackendCapabilities()


def _log_backend_status() -> None:
    """
    Log toolchain version information and any detected incompatibilities.

    Called once at module load.  Log levels:

    - DEBUG   — detailed version strings (numba, llvmlite, LLVM, driver,
                runtime, PTX, ptxas, GPU names)
    - INFO    — one-line summary of available backends
    - WARNING — non-fatal issues (e.g. ptxas not on PATH, minor version gaps)
    - CRITICAL — incompatibilities that will prevent a backend from working
                 (e.g. driver older than runtime, probe kernel failed)
    """
    avail = backends.available()
    logger.info("Available backends: %s  (best: %s)", ", ".join(avail), avail[-1])

    # ── CUDA toolchain ────────────────────────────────────────────────────────
    cuda = backends.cuda_info
    if cuda.numba_version is not None:  # numba is installed
        _fv = lambda v: f"{v[0]}.{v[1]}" if v else "unknown"
        logger.debug(
            "CUDA toolchain — numba %s | llvmlite %s | LLVM %s | "
            "driver CUDA %s | runtime CUDA %s | PTX %s",
            cuda.numba_version,
            cuda.llvmlite_version or "?",
            cuda.llvm_version or "?",
            _fv(cuda.cuda_driver_version),
            _fv(cuda.cuda_runtime_version),
            cuda.ptx_version or "? (probe not reached)",
        )
        if cuda.ptxas_path:
            logger.debug("ptxas — %s  (%s)", cuda.ptxas_path, cuda.ptxas_version or "?")
        for d in cuda.devices:
            cc = d["compute_capability"]
            logger.debug("  CUDA device: %s  (CC %d.%d)", d["name"], cc[0], cc[1])
        for w in cuda.warnings:
            logger.warning("CUDA toolchain: %s", w)
        for e in cuda.errors:
            logger.critical("CUDA toolchain: %s", e)

    # ── MLX toolchain ─────────────────────────────────────────────────────────
    mlx = backends.mlx_info
    if mlx.mlx_version is not None:  # mlx is installed
        mem_str = (
            f"{mlx.memory_size / (1024 ** 3):.0f} GB"
            if mlx.memory_size else "unknown"
        )
        logger.debug(
            "MLX toolchain — mlx %s | GPU: %s | memory: %s | Metal: %s | macOS: %s",
            mlx.mlx_version,
            mlx.gpu_name or "unknown",
            mem_str,
            mlx.metal_version or "unknown",
            mlx.macos_version or "unknown",
        )
        for w in mlx.warnings:
            logger.warning("MLX toolchain: %s", w)
        for e in mlx.errors:
            logger.critical("MLX toolchain: %s", e)


_log_backend_status()


