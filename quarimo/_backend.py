def _probe_cuda_toolchain() -> CUDAToolchainInfo:
    """
    Collect CUDA / Numba / LLVM version information and verify end-to-end
    kernel compilation by compiling and running a trivial probe kernel.

    The probe proceeds step by step and stops early when a required component
    is absent.  Incompatibilities are recorded in ``errors``; non-fatal issues
    go into ``warnings``.  The returned object's ``available`` flag is True
    only if the probe kernel compiled and executed without error.
    """
    info = CUDAToolchainInfo(available=False)

    # ── numba ─────────────────────────────────────────────────────────────────
    try:
        import numba
        info.numba_version = numba.__version__
    except ImportError:
        return info  # numba not installed — not an error, just unavailable

    # ── llvmlite / LLVM ───────────────────────────────────────────────────────
    # llvmlite provides the NVPTX backend that generates PTX from numba kernels.
    # Its version (and the bundled LLVM version) determines which PTX ISA
    # version is emitted — a mismatch with the system ptxas is the most common
    # source of CUDA_ERROR_UNSUPPORTED_PTX_VERSION failures.
    try:
        import llvmlite
        info.llvmlite_version = llvmlite.__version__
    except ImportError as e:
        info.warnings.append(f"llvmlite not importable: {e}")

    try:
        import llvmlite.binding as llvm_binding
        info.llvm_version = ".".join(str(x) for x in llvm_binding.llvm_version_info[:3])
    except Exception as e:
        info.warnings.append(f"Could not read LLVM version from llvmlite.binding: {e}")

    # ── CUDA availability ──────────────────────────────────────────────────────
    try:
        from numba import cuda
        if not cuda.is_available():
            return info  # no GPU or driver — not an error
    except Exception as e:
        info.errors.append(f"numba.cuda import or availability check failed: {e}")
        return info

    # ── CUDA driver version ───────────────────────────────────────────────────
    try:
        from numba.cuda.cudadrv.driver import driver as _drv
        info.cuda_driver_version = _drv.get_version()
    except Exception as e:
        info.warnings.append(f"Could not query CUDA driver version: {e}")

    # ── CUDA runtime version ──────────────────────────────────────────────────
    try:
        from numba.cuda.cudadrv.runtime import runtime as _rt
        info.cuda_runtime_version = _rt.get_version()
    except Exception as e:
        info.warnings.append(f"Could not query CUDA runtime version: {e}")

    # ── Driver / runtime compatibility ────────────────────────────────────────
    if info.cuda_driver_version and info.cuda_runtime_version:
        dv = info.cuda_driver_version
        rv = info.cuda_runtime_version
        if dv < rv:
            info.errors.append(
                f"CUDA driver ({dv[0]}.{dv[1]}) is older than the runtime "
                f"({rv[0]}.{rv[1]}). The driver must be at least as new as "
                "the CUDA runtime. Upgrade the driver or install an older "
                "cuda-toolkit to match."
            )

    # ── GPU devices ───────────────────────────────────────────────────────────
    try:
        from numba import cuda
        for device in cuda.gpus:
            with device:
                name = device.name
                if isinstance(name, bytes):
                    name = name.decode()
                info.devices.append({
                    "name": name,
                    "compute_capability": device.compute_capability,
                })
    except Exception as e:
        info.warnings.append(f"Could not enumerate CUDA devices: {e}")

    # ── ptxas ─────────────────────────────────────────────────────────────────
    # ptxas assembles PTX into device binary (CUBIN).  Its version must be >=
    # the PTX ISA version emitted by llvmlite's NVPTX backend.
    ptxas_path = shutil.which("ptxas")
    if ptxas_path:
        info.ptxas_path = ptxas_path
        try:
            result = subprocess.run(
                [ptxas_path, "--version"],
                capture_output=True, text=True, timeout=5,
            )
            lines = (result.stdout + result.stderr).strip().splitlines()
            info.ptxas_version = lines[0] if lines else None
        except Exception as e:
            info.warnings.append(f"Could not run ptxas --version: {e}")
    else:
        info.warnings.append(
            "ptxas not found on PATH. Numba requires ptxas to assemble CUDA "
            "kernels. Add the CUDA toolkit bin directory to PATH (e.g. "
            "module load cuda, or set CUDA_HOME)."
        )

    # ── End-to-end probe kernel ───────────────────────────────────────────────
    # Compiling and executing a trivial kernel catches PTX version mismatches,
    # linker errors, and any other toolchain incompatibilities that only surface
    # at JIT-compile time — including CUDA_ERROR_UNSUPPORTED_PTX_VERSION, which
    # occurs when llvmlite emits a PTX ISA version newer than ptxas supports.
    try:
        import warnings
        import numpy as np
        from numba import cuda
        from numba.core.errors import NumbaPerformanceWarning

        @cuda.jit(cache=False)
        def _probe(x):
            i = cuda.grid(1)
            if i < x.shape[0]:
                x[i] = x[i] + 1.0

        arr = cuda.to_device(np.zeros(1, dtype=np.float32))
        with warnings.catch_warnings():
            warnings.simplefilter("ignore", NumbaPerformanceWarning)
            _probe[1, 1](arr)
        cuda.synchronize()

        # Extract the PTX .version directive from the compiled kernel.
        # This is the PTX ISA version llvmlite generated — if it is higher
        # than what ptxas supports, the probe above will have already failed.
        try:
            for ptx in _probe.inspect_asm().values():
                for line in ptx.splitlines():
                    m = re.match(r"\s*\.version\s+(\S+)", line)
                    if m:
                        info.ptx_version = m.group(1)
                        break
                if info.ptx_version:
                    break
        except Exception as e:
            info.warnings.append(f"Could not extract PTX version from probe kernel: {e}")

        info.available = True

    except Exception as e:
        info.errors.append(
            f"CUDA probe kernel failed to compile or run: {e}. "
            "Check that ptxas, the CUDA driver, and the CUDA runtime are all "
            "from compatible versions. Common cause: llvmlite emits a PTX ISA "
            "version newer than the ptxas on PATH supports — verify with "
            "'ptxas --version' and compare to the PTX version logged above."
        )

    return info


def _probe_mlx_toolchain() -> MLXToolchainInfo:
    """
    Collect MLX / Metal version information and verify that a probe
    computation can run on the Metal GPU.

    Stops early if MLX is not installed (not an error — just unavailable).
    """
    info = MLXToolchainInfo(available=False)

    # ── mlx ───────────────────────────────────────────────────────────────────
    try:
        import mlx.core as mx
        info.mlx_version = mx.__version__
    except ImportError:
        return info  # mlx not installed — not an error

    # ── macOS version ─────────────────────────────────────────────────────────
    try:
        info.macos_version = platform.mac_ver()[0] or None
    except Exception:
        pass

    # ── Metal device info ─────────────────────────────────────────────────────
    try:
        device_info = mx.metal.device_info()
        info.gpu_name = device_info.get("device_name")
        info.memory_size = device_info.get("memory_size")
        raw_mv = device_info.get("metal_version")
        info.metal_version = str(raw_mv) if raw_mv is not None else None
    except Exception as e:
        info.warnings.append(f"Could not query Metal device info: {e}")

    # ── Probe computation ─────────────────────────────────────────────────────
    try:
        import mlx.core as mx
        mx.eval(mx.array([1.0, 2.0, 3.0], dtype=mx.float32) * 2.0)
        info.available = True
    except Exception as e:
        info.errors.append(f"MLX probe computation failed: {e}")

    return info


