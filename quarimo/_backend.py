class _BackendCapabilities:
    """
    Lazy, cached detection of all available quarimo backends.

    Use the module-level ``backends`` singleton rather than instantiating this
    class directly.  Properties are evaluated at most once per session.

    Properties
    ----------
    numba : bool
        True if the ``numba`` package is importable.  Required for both the
        ``cpu-parallel`` and ``cuda`` backends.
    cuda : bool
        True if the CUDA probe kernel compiled and ran successfully.
        Implies numba is available and a compatible GPU + driver are present.
    mlx : bool
        True if the MLX probe computation ran successfully on a Metal GPU.
        Apple Silicon only; mutually exclusive with ``cuda`` in practice.
    cuda_info : CUDAToolchainInfo
        Full toolchain snapshot for the CUDA backend.
    mlx_info : MLXToolchainInfo
        Full toolchain snapshot for the MLX backend.
    """

    @cached_property
    def numba(self) -> bool:
        """True if Numba is installed (enables ``cpu-parallel`` backend)."""
        try:
            import numba  # noqa: F401
            return True
        except ImportError:
            return False

    @cached_property
    def cuda_info(self) -> CUDAToolchainInfo:
        """Full CUDA toolchain snapshot (computed once, cached)."""
        return _probe_cuda_toolchain()

    @cached_property
    def mlx_info(self) -> MLXToolchainInfo:
        """Full MLX toolchain snapshot (computed once, cached)."""
        return _probe_mlx_toolchain()

    @cached_property
    def cuda(self) -> bool:
        """True if Numba + a CUDA-capable GPU are present (enables ``cuda`` backend)."""
        return self.cuda_info.available

    @cached_property
    def mlx(self) -> bool:
        """True if MLX + Metal GPU are available (enables ``mlx`` backend, Apple Silicon)."""
        return self.mlx_info.available

    # ------------------------------------------------------------------
    # Derived helpers
    # ------------------------------------------------------------------

    def available(self) -> List[str]:
        """
        Return available backend names in ascending preference order.

        The last element is always the best (highest-performance) backend.

        Examples
        --------
        >>> backends.available()
        ['python', 'cpu-parallel', 'cuda']   # NVIDIA Linux
        >>> backends.available()
        ['python', 'cpu-parallel', 'mlx']    # Apple Silicon
        >>> backends.available()
        ['python']                           # minimal install
        """
        result = ["python"]
        if self.numba:
            result.append("cpu-parallel")
        if self.mlx:
            result.append("mlx")
        if self.cuda:
            result.append("cuda")
        return result

    def best(self) -> str:
        """Return the highest-priority available backend name."""
        return self.available()[-1]

    def resolve_for_kernel(self, kernel: str, backend: str) -> Tuple[str, Optional[str]]:
        """
        Resolve a backend for a specific kernel, with graceful fallback.

        If the resolved backend has no implementation for *kernel*, the
        next-best available backend that does is returned instead.

        Parameters
        ----------
        kernel : str
            Key in ``KERNEL_IMPLEMENTATIONS`` (e.g. ``'quartet_topology'``).
        backend : str
            Requested backend name or ``'best'``.

        Returns
        -------
        (effective, original)
            *effective* is the backend to use.  *original* is the originally
            requested backend when a fallback occurred, otherwise ``None``.
        """
        resolved = self.resolve(backend)  # raises ValueError if unavailable
        supported = KERNEL_IMPLEMENTATIONS.get(kernel, frozenset())
        if resolved in supported:
            return resolved, None
        # Fall back to the best available backend that has this kernel
        fallback = next(
            (b for b in reversed(self.available()) if b in supported),
            "python",
        )
        return fallback, resolved

    def status(self) -> List[Tuple[str, bool]]:
        """
        Return all known backends in ascending priority order with availability.

        Each entry is ``(name, is_available)``.  New backends must be added
        here so that ``log_backend_availability`` reports them automatically.

        Returns
        -------
        list[tuple[str, bool]]
            e.g. ``[('python', True), ('cpu-parallel', True), ('mlx', False), ('cuda', False)]``
        """
        return [
            ("python", True),
            ("cpu-parallel", self.numba),
            ("mlx", self.mlx),
            ("cuda", self.cuda),
        ]

    def resolve(self, backend: str) -> str:
        """
        Validate and return a concrete backend name.

        Parameters
        ----------
        backend : str
            ``'best'`` or an explicit backend name (``'python'``,
            ``'cpu-parallel'``, ``'cuda'``, ``'mlx'``).

        Returns
        -------
        str
            The resolved backend name.

        Raises
        ------
        ValueError
            If *backend* is not in ``available()``.
        """
        if backend == "best":
            return self.best()
        available = self.available()
        if backend not in available:
            raise ValueError(
                f"Backend '{backend}' not available. "
                f"Available backends: {', '.join(available)}"
            )
        return backend


