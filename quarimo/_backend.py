@dataclass
class CUDAToolchainInfo:
    """
    Version and compatibility snapshot of the CUDA / Numba / LLVM toolchain.

    Populated by ``_probe_cuda_toolchain()``.  Errors indicate incompatibilities
    that will prevent the CUDA backend from functioning; warnings are non-fatal
    (e.g. ptxas not on PATH).

    Attributes
    ----------
    available : bool
        True only if a probe kernel compiled and ran successfully end-to-end.
    numba_version : str or None
        Installed numba version string, or None if numba is not installed.
    llvmlite_version : str or None
        Installed llvmlite version string.  llvmlite provides the LLVM/NVPTX
        backend that generates PTX from numba-compiled kernels.
    llvm_version : str or None
        LLVM version bundled with llvmlite (e.g. ``"14.0.6"``).
    cuda_driver_version : (int, int) or None
        CUDA driver version reported by the kernel driver, as (major, minor).
        Must be >= cuda_runtime_version for forward compatibility.
    cuda_runtime_version : (int, int) or None
        CUDA runtime version in the active environment, as (major, minor).
    ptx_version : str or None
        PTX ISA ``.version`` from the compiled probe kernel (e.g. ``"8.5"``).
        Generated by llvmlite's NVPTX backend; must be supported by ptxas.
    ptxas_path : str or None
        Absolute path to the ``ptxas`` assembler found on PATH.
    ptxas_version : str or None
        First line of ``ptxas --version`` output.
    devices : list[dict]
        One entry per visible CUDA device: ``{"name": str, "compute_capability": (int, int)}``.
    errors : list[str]
        Incompatibilities that prevent the CUDA backend from functioning.
        Each entry is logged at CRITICAL level.
    warnings : list[str]
        Non-fatal issues.  Each entry is logged at WARNING level.
    """

    available: bool
    numba_version: str | None = None
    llvmlite_version: str | None = None
    llvm_version: str | None = None
    cuda_driver_version: tuple[int, int] | None = None
    cuda_runtime_version: tuple[int, int] | None = None
    ptx_version: str | None = None
    ptxas_path: str | None = None
    ptxas_version: str | None = None
    devices: list[dict] = field(default_factory=list)
    errors: list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


@dataclass
class MLXToolchainInfo:
    """
    Version and hardware snapshot of the MLX / Metal toolchain.

    Populated by ``_probe_mlx_toolchain()``.

    Attributes
    ----------
    available : bool
        True only if a probe computation ran successfully on the Metal GPU.
    mlx_version : str or None
        Installed mlx version string, or None if mlx is not installed.
    gpu_name : str or None
        Metal GPU device name (e.g. ``"Apple M3 Pro"``).
    memory_size : int or None
        Total unified memory in bytes as reported by Metal.
    metal_version : str or None
        Metal API version string from ``mx.metal.device_info()``.
    macos_version : str or None
        macOS version string (e.g. ``"14.5"``).
    errors : list[str]
        Incompatibilities that prevent the MLX backend from functioning.
    warnings : list[str]
        Non-fatal issues.
    """

    available: bool
    mlx_version: str | None = None
    gpu_name: str | None = None
    memory_size: int | None = None
    metal_version: str | None = None
    macos_version: str | None = None
    errors: list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


