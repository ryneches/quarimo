def check_numba_available() -> bool:
    """Return True if Numba is installed."""
    return backends.numba


def check_cuda_available() -> Tuple[bool, bool]:
    """Return ``(numba_available, cuda_available)`` for backward compatibility."""
    return (backends.numba, backends.cuda)


def check_mlx_available() -> bool:
    """Return True if MLX with Metal GPU is available (Apple Silicon)."""
    return backends.mlx


def get_available_backends() -> List[str]:
    """Return available backend names in ascending preference order."""
    return backends.available()


def get_best_backend() -> str:
    """Return the highest-priority available backend name."""
    return backends.best()


def resolve_backend(backend: str) -> str:
    """Validate and resolve a backend name; raises ValueError if unavailable."""
    return backends.resolve(backend)


def get_backend_info() -> dict:
    """
    Return a comprehensive dict of backend availability and toolchain status.

    The ``cuda_toolchain`` and ``mlx_toolchain`` sub-dicts mirror the fields
    of ``CUDAToolchainInfo`` and ``MLXToolchainInfo`` respectively.  They are
    ``None`` when the corresponding package (numba / mlx) is not installed.
    """
    cuda = backends.cuda_info
    mlx = backends.mlx_info

    return {
        "numba_available":  backends.numba,
        "cuda_available":   backends.cuda,
        "mlx_available":    backends.mlx,
        "backends":         backends.available(),
        "best_backend":     backends.best(),
        "cuda_toolchain": {
            "numba_version":        cuda.numba_version,
            "llvmlite_version":     cuda.llvmlite_version,
            "llvm_version":         cuda.llvm_version,
            "cuda_driver_version":  cuda.cuda_driver_version,
            "cuda_runtime_version": cuda.cuda_runtime_version,
            "ptx_version":          cuda.ptx_version,
            "ptxas_path":           cuda.ptxas_path,
            "ptxas_version":        cuda.ptxas_version,
            "devices":              cuda.devices,
            "errors":               cuda.errors,
            "warnings":             cuda.warnings,
        } if cuda.numba_version is not None else None,
        "mlx_toolchain": {
            "mlx_version":   mlx.mlx_version,
            "gpu_name":      mlx.gpu_name,
            "memory_size":   mlx.memory_size,
            "metal_version": mlx.metal_version,
            "macos_version": mlx.macos_version,
            "errors":        mlx.errors,
            "warnings":      mlx.warnings,
        } if mlx.mlx_version is not None else None,
    }
